	<h1>Sobre o VII Encontro da Matemática</h1>
<p class="logotipo"><img src="images/math_logo.gif" width="135" height="118" /></p>
<p>O Encontro da Matemática é um dos maiores eventos matemáticos do interior do estado do Ceará, tendo sido freqüentado por um publico excelente de alunos, professores, técnicos e da comunidade em geral. Nas suas seis edições anteriores vem mantendo uma média de mais de 400 participantes sendo que recebe um publico vindo não apenas da cidade de Cedro, mas também de outras localidades do estado como Fortaleza, Maracanaú, Sobral, Boa Viagem, Juazeiro do Norte, Crato, Iguatu, Icó, entre outras, como também de outros estados como Paraíba, Pernambuco, Bahia, entre outros. A qualidade dos eventos, a organização e a estrutura sempre chamam a atenção de modo que todos os visitantes já se sentem convidados e ansiosos pela edição posterior do evento.</p>
<p>Em sua sétima edição, com o tema “Integrando Novas Ideias”, vem Integrar a I SECITIF – Semana de Ciência e Tecnologia do IFCE – Campus Cedro e mantém o foco de propiciar aos participantes um ambiente de enriquecimento científico através da execução de diversas oficinas e palestras ministradas por professores capacitados e de alto gabarito acadêmico. Além disso, estará realizando a Primeira edição do Campeonato de Xadrez e estará disponibilizando uma experiência inovadora sobre a ludicidade e os jogos matemáticos.</p>
