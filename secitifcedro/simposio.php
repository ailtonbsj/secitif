    <h1>Sobre o I Simpósio Técnico do IFCE – Campus Cedro</h1>
    <p class="logotipo"><img src="images/simposio_logo.gif" width="158" height="122" /></p>
    <p>O I Simpósio Técnico do IFCE – Campus Cedro é um evento que faz parte do hall de realizações da I semana de Ciência e tecnologia do IFCE – Campus Cedro e tem o intuito de proporcionar a troca de experiências e resultados de projetos entre professores, pesquisadores e estudantes das diversas redes de ensino e pesquisadores em geral, bem como empresários, industriais e profissionais autônomos.</p>
    <p>O evento será composto de sessões de apresentações de banners de artigos científicos avaliados e validados por uma comissão altamente conceituada e competições técnico-científicas.</p>
    <p>A submissão de Trabalhos se dará entre os dias <b>01 a 31 de outubro</b> de 2012 segundo o calendário no edital disponível <a href="downloads/edital_simposio.pdf">aqui</a>.</p>
    <p>Serão realizadas durante o evento as diversas competições de cunho técnico:</p>
    <ul>
    <li>I Maratona de Programação</li>
    <li>I Corrida mecânica</li>
    <li>I Campeonato da Eletrotécnica – Projeto, Elaboração e Montagem de Circuitos Elétricos</li>
    </ul>
      <p>O link para os editais das competições estam disponíveis <a href="downloads.php">aqui</a>.</p>
      <p>Local: Campus Cedro do IFCE – Cedro - CE.<br />
      Período: 20 a 22 de outubro de 2012.<br />
      Aguardamos você e a sua comissão!<br />
      Maiores informações entrar em contato pelos telefones (88)3564-1430 e (88)3564-1000</p>
      <h2>Passos para submissões de trabalhos</h2>
      <p>Para submeter seu artigo siga os passos:</p>
      <p>1) Você primeiro deve <strong>se cadastrar no site</strong> do SECITIF. Caso  ainda não tenha se cadastrado <a href="inscricao.php" target="_blank">clique aqui para iniciar</a>;</p>
      <p>2) <strong>Confirme sua inscrição pelo e-mail de resposta</strong> que foi  enviado automaticamente para o <strong>e-mail digitado no ato de inscrição</strong>. (Verifique  a <em>caixa de span</em>, caso não encontre o e-mail);</p>
      <p>3) Agora é necessário<strong> Fazer o Login na pagina inicial</strong> com o  <strong>e-mail e senha</strong>. <a href="index.php" target="_blank">Vá para pagina inicial e faça o login</a>;</p>
      <p>4) Depois de fazer login Selecione o Banner <img src="images/box_3.gif" width="339" height="78" align="middle" />;</p>
      <p>5) Preencha o formulário <em><strong>Enviar Artigo para o I Simpósio  Técnico</strong></em> e<strong> selecione o arquivo a ser enviado</strong>, depois clique em <strong>Enviar artigo</strong>. (<em>Nota:  o arquivo tem que está no formato Word 2007 extensão .docx, tamanho máximo 2MB e  seguir o Modelo disponível na sessão <a href="downloads.php" target="_blank">downloads</a></em>);</p>
      <p>6) Caso seu artigo tenha sido enviado com sucesso,<strong> uma caixa  verde</strong> com o texto <strong style="color:#009900;">PARABENS!!! Seu artigo foi enviado com Sucesso!!!</strong> será  exibido.</p>
